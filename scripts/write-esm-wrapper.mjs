import fs from 'node:fs';
import path from 'node:path';
import { createRequire } from 'node:module';

const repoRoot = path.resolve(new URL('..', import.meta.url).pathname);
const cjsPath = path.join(repoRoot, 'dist', 'index.cjs');
const mjsPath = path.join(repoRoot, 'dist', 'index.mjs');
const require = createRequire(import.meta.url);

if (!fs.existsSync(cjsPath)) {
  throw new Error(`Cannot generate Polymer ESM wrapper; missing ${path.relative(repoRoot, cjsPath)}`);
}

const polymer = require(cjsPath);
const exportNames = Object.keys(polymer)
  .filter((name) => /^[A-Za-z_$][0-9A-Za-z_$]*$/.test(name))
  .sort();

const lines = [
  '// Generated by scripts/write-esm-wrapper.mjs after the Shadow CLJS build.',
  '// Shadow CLJS emits the agency runtime as CommonJS; Vite/LoomLarge consume this ESM facade.',
  "import polymerRuntime from './index.cjs';",
  '',
  'const polymer = polymerRuntime && polymerRuntime.default ? polymerRuntime.default : polymerRuntime;',
  '',
];

for (const name of exportNames) {
  lines.push(`export const ${name} = polymer[${JSON.stringify(name)}];`);
}

lines.push('');
lines.push('export default polymer;');
lines.push('');

fs.writeFileSync(mjsPath, lines.join('\n'));
console.log(`Wrote ${path.relative(repoRoot, mjsPath)} with ${exportNames.length} named exports.`);
